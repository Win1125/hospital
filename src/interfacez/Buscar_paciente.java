    public void mostrar_pacientes() {
        String Query = "select `p`.`ID_Paciente` AS `ID_Paciente`,concat(`p`.`Nombres`,' ',`p`.`Apellidos`) AS `NombreCompleto`,`p`.`CorreoElectronico` AS `CorreoElectronico`,concat(`ln`.`NombreLugar`,' - ',`p`.`FechaNacimiento`) AS `LugarFechaNacimiento`,`p`.`DireccionResidencia` AS `Direccion`,`p`.`TelefonoFijo` AS `Telefono`,`c`.`NombreCiudad` AS `CiudadAfiliacionEPS` from ((`hospital`.`pacientes` `p` join `hospital`.`ciudades` `c` on(`p`.`CiudadAfiliacionEPS` = `c`.`CiudadID`)) join `hospital`.`lugaresnacimiento` `ln` on(`p`.`LugarNacimiento` = `ln`.`ID_LugarNacimiento`))";
        model = new DefaultTableModel(null, Campos_tabla);
        try {
            Statement st = (Statement) conectar.createStatement();
            ResultSet rs = st.executeQuery(Query);

            while (rs.next()) {
                informacion[0] = rs.getString("ID_Paciente");
                informacion[1] = rs.getString("NombreCompleto");
                informacion[2] = rs.getString("CorreoElectronico");
                informacion[3] = rs.getString("LugarFechaNacimiento");
                informacion[4] = rs.getString("Direccion");
                informacion[5] = rs.getString("Telefono");
                informacion[6] = rs.getString("CiudadAfiliacionEPS");
                model.addRow(informacion);
            }
            tabla_pacientes.setModel(model);
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

